LIST p=18f4520
#include <p18f4520.inc>
	CONFIG OSC = INTIO67 
	CONFIG WDT = OFF 
	ORG 0x100

START:
    CLRF 0x000
    MOVLW 0xCF
    MOVWF 0x001
    MOVLW 0XFB
    MOVWF 0x002
    
    MOVLW D'8'
    MOVWF TRISA
    
    LOOP:
	RLCF 0x001
	RRCF 0x000
	DECF TRISA
	BNZ LOOP
	
    MOVFF 0x000,WREG
    SUBWF 0x002,W
    BZ SAME
    BNZ DIFFERENT
    SAME:
	MOVLW 0xFF
	MOVWF 0x03
	GOTO TERMINATE
    DIFFERENT:
	MOVLW 0x01
	MOVWF 0x03
	GOTO TERMINATE
TERMINATE:
    END


