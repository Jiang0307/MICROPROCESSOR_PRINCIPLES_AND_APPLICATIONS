LIST p=18f4520
#include <p18f4520.inc>
	CONFIG OSC = INTIO67 
	CONFIG WDT = OFF 
	org 0x100
	
START:
    MOVLW D'9'
    MOVWF TRISA
    CLRF WREG
    LFSR 0,0x100
    LOOP1:
	MOVFF WREG,POSTINC0
	INCF WREG
	DECF TRISA
	BNZ LOOP1
	
    LFSR 0,0x100
    LFSR 1,0x120
    MOVLW D'9'
    MOVWF TRISA
    CLRF WREG
    LOOP3:
	ADDWF POSTINC0,W
	MOVFF WREG,POSTINC1
	DECF TRISA
	BNZ LOOP3
END


